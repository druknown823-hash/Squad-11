# Get the length of the rectangle from the user and convert it into a number
length = float(input("Enter the length of the rectangle: ")) 

# Get the width of the rectangle from the user and convert it into a number
width = float(input("Enter the width of the rectangle: ")) 

# The area is calculated by multiplying the length by the width
area = length * width 

# Show the user the final calculated area
print("The area of the rectangle is:", area)